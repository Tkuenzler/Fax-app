	public static String Filter(Record record) {
		switch(record.getPcn()) {
			case "COPAY":
			case "TEHS1BTA":
			case "COSBERG":
			case "MD":
				return InsuranceType.MEDICARE_COMMERCIAL;
			case "MEDDPRIME":
				return  checkMeddprime(record);
			case "A4":
			case "PEU":
			case "No Data Returned":
			case "NO DATA RETURNED":
				return FilterByGroup(record);
			default: 
				if(record.getPcn().equalsIgnoreCase(""))
					return InsuranceType.PRIVATE_UNKNOWN;
				else
					return InsuranceType.MEDICARE_COMMERCIAL;
		}
	}
	private static String FilterByGroup(Record record) {
		if(record.getGrp().startsWith("LHPG") && record.getState().equalsIgnoreCase("NY"))
			return InsuranceType.MEDICAID;
		if(record.getGrp().startsWith("MMO") || record.getGrp().startsWith("845"))
			return InsuranceType.MEDICAID;
		if(record.getGrp().startsWith("TXS")) 
			return InsuranceType.TRICARE;
		if(record.getGrp().startsWith("BMI") )
			return InsuranceType.PRIVATE_VERIFIED;
		if(record.getGrp().startsWith("WMHR") || record.getGrp().startsWith("BCP"))
			return InsuranceType.PRIVATE_NO_TELMED;
		if(CheckPaid(record.getGrp()))
			return InsuranceType.PRIVATE_VERIFIED;
		else if(CheckNoTelmed(record.getGrp()))
			return InsuranceType.PRIVATE_NO_TELMED;
		else if(CheckMedicaid(record.getGrp()))
			return InsuranceType.MEDICAID;
		else if(CheckNotCovered(record.getGrp()))
			return InsuranceType.NOT_COVERED;
		else if(CheckOutOfNetwork(record.getGrp()))
			return InsuranceType.OUT_OF_NETWORK;
		else
			return InsuranceType.PRIVATE_UNKNOWN;
	}
	private static boolean CheckNoTelmed(String grp) {
		switch(grp) {
			case "URMBT00120":
				return true;
			default:
				return false;
		}
	}
